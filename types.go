package main

import "fmt"

func main() {
	// Целое число (int)
	возраст := 25

	// Текст (string)
	имя := "Давид"

	// Дробное число (float)
	рост := 1.75

	// Да/Нет (bool)
	learning := true

	fmt.Println("Имя", имя)
	fmt.Println("Возраст", возраст)
	fmt.Println("Рост", рост)
	fmt.Println("Учу Go", learning)
}
